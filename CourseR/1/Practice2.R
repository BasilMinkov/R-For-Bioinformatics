x = runif(1, 0, 3)
sin.x = sin(x)
y = 0
a = 0

for (i in 1:10){
  y = y + (((-1)**(i+1)) * ((x**(2*i-1))/factorial(2*i-1)))
  a[i] = abs(sin.x - y)
}

###################################################

par(mfrow=c(1,1))
plot(1:10, a, t="l", log="y")

