d = 0
xs = runif(100, -10, 10)

for (j in 1:100){
  y = 0
  for (i in 1:100){
    y = y + (((-1)**(i+1)) * ((xs[j]**(2*i-1))/factorial(2*i-1)))
  }
  d[j] = abs(y - sin(xs[j]))
}

o = order(xs)
par(mfrow=c(1,1))
plot(xs[o], d[o], log='y')
